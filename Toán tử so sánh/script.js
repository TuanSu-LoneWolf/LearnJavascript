//So sánh tương đối
/**
 * ==
 * !=
 */

var a = 1;
var b = "1";
console.log("Cho a = 1, b = '1'");
console.log("//So sánh tương đối");
console.log("Bằng");
console.log(a == b);
console.log("Khác");
console.log(a != b);

//So sánh tuyệt đối
/**
 * ===
 * !==
 */
